namespace MemoryToolKit.MemoryUtils;

/// <summary>
///     Provides different types of pointer dereferencing.
/// </summary>
public enum DerefType
{
	/// <summary>
	///     Automatic pointer dereferencing.
	/// </summary>
	Auto,

	/// <summary>
	///     32-bit pointer dereferencing.
	/// </summary>
	Bit32,

	/// <summary>
	///     64-bit pointer dereferencing.
	/// </summary>
	Bit64
}